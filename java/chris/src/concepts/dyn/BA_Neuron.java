    public NormalizationType normalization_type() {
        return NormalizationType.BIN;
    } 

    /**
     * Setter.
     * @param activation
     */
    @Override
    public void set_activation(float activation) {
        throw new Crash("Activation cannot be directly set for this concept");
    }

